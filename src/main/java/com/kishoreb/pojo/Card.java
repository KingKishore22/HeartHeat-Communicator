package com.kishoreb.pojo;

public class Card {
String fir;
String sec;
String thi;
String fort;
public String getFir() {
	return fir;
}
public void setFir(String fir) {
	this.fir = fir;
}
public String getSec() {
	return sec;
}
public void setSec(String sec) {
	this.sec = sec;
}
public String getThi() {
	return thi;
}
public void setThi(String thi) {
	this.thi = thi;
}
public String getFort() {
	return fort;
}
public void setFort(String fort) {
	this.fort = fort;
}
		
}
